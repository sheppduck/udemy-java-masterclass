package com.joel;

public class Main {

    public static void main(String[] args) {
        int myIntValue = 5 / 2;
        float myFloatValue = 5f / 3f; //add f to end of num
        double myDoubleValue = 5d / 3d; //add d to end of num but decimal its assumed a double
        System.out.println("myIntValue = " + myIntValue);
        System.out.println("myFloatValue = " + myFloatValue);
        System.out.println("myDoubleValue =" + myDoubleValue);



    }
}
